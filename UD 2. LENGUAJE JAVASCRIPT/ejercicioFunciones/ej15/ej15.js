let dia = prompt("Introduce el dia de la semana");

switch(dia){
    case "Lunes":
        alert("Martes");
        break;
    case "Martes":
        alert("Miercoles");
        break;
    case "Miercoles":
        alert("Jueves");
        break;
    case "Jueves":
        alert("Viernes");
        break;
    case "Viernes":
        alert("Sabado");
        break;
    case "Sabado":
        alert("Domingo");
        break;
    case "Domingo":
        alert("Lunes");
        break;
}